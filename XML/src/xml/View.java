    public void sql() throws ClassNotFoundException, SQLException {
        Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");

        Connection con = DriverManager.getConnection("jdbc:sqlserver:SQLMASTER\\SQLAMSTER;databaseName=Elecciones", "sa", "Bases2018");

        CallableStatement proc_stmt = con.prepareCall("{ call getCantones(?) }");

        proc_stmt.setString(1, "1");
        ResultSet rs = proc_stmt.executeQuery();

        if (rs.next()) {
            int employeeId = rs.getInt(1);
            System.out.println("Generated employeeId: " + employeeId);
        } else {
            System.out.println("Stored procedure couldn't generate new Id");
        }
    }

